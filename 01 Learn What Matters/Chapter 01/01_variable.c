#include <stdio.h>

int main()
{
    // Integer variable
    int age = 25;
    // Floating-point variable
    float height = 5.9;
    // Character variable
    char initial = 'A';

    // Print the variables
    printf("Age: %d\n", age);
    printf("Height: %.1f\n", height);
    printf("Initial: %c\n", initial);

    return 0;
}
